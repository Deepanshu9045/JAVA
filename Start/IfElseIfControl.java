public class IfElseIfControl
{
    public static void main(String[] args)
    {
        String city="Delhi";
        if(city=="Meeruit")
        {
            System.out.println("City is Meeruit");
        }
        else if(city=="Agra")
        {
            System.out.println("City is Agra");
        }
        else if(city=="Noida")
        {
            System.out.println("City is Noida");
        }
        else
        {
            System.out.println(city);
        }
    }
}